  const [color, shade] = props.color.split('-');
  const defaultShade = '500';
  return `radial-gradient(circle, ${lightColors[color]?.[shade] || lightColors.blue[defaultShade]} 0%, rgba(0, 0, 0, 0) 70%)`;
});

const handleMouseMove = (event: MouseEvent) => {
  if (lightRef.value) {
    const lightSize = 450;
    lightRef.value.style.transform =
      `translate(${event.clientX - lightSize / 2}px,
       ${event.clientY - lightSize / 2}px)`;
  }
};

